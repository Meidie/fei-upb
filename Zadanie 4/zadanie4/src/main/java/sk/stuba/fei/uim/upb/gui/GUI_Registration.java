    private void registration(MouseEvent evt) {//GEN-FIRST:event_Registracia
        try {
        	
		    	if(jTextField1.getText().isEmpty()) {
		    		JOptionPane.showMessageDialog(rootPane, "Nevyplnené meno.");
		    	} else if (jPasswordField3.getPassword().length == 0) {
		    		JOptionPane.showMessageDialog(rootPane, "Nevyplnené heslo.");
		    	} else if (Arrays.equals(jPasswordField3.getPassword(), jPasswordField2.getPassword())){
                    MyResult tmp = registration.register(jTextField1.getText(), String.valueOf(jPasswordField2.getPassword()));
                    if (!tmp.getFirst())
                        JOptionPane.showMessageDialog(rootPane, tmp.getMsg());
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane, "Registrácia prebehla úspešne.");
                        dispose();
                    }
                }
                else
                    JOptionPane.showMessageDialog(rootPane, "Heslá sa nezhodujú.");
            } catch (Exception ex) {
                Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        
    }//GEN-LAST:event_Registracia

